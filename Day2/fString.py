# f-string is a way to format strings in Python
# It allows you to embed expressions inside string literals, using curly braces {}
# f-strings are prefixed with 'f' or 'F'
# f-strings are more readable and concise than the older string formatting methods
# f-strings are available in Python 3.6 and later
# f-strings are faster than the older string formatting methods 

name = 'John'
age = 30
height = 1.75
weight = 70.5

# Using f-string to format the string
formatted_string = f"My name is {name}, I am {age} years old, my height is {height} m, and my weight is {weight} kg."
print(formatted_string)
# Output: My name is John, I am 30 years old, my height is 1.75 m, and my weight is 70.5 kg.